void SmoothSensorData(float *newData, float *oldData, float mag)
{
	for (int i=0; i<3; i++)
	{
		float diff = abs(oldData[i]-newData[i]);
		float chg = diff / mag;
		if (chg > 0.2f)
			chg = 1.0f;
		oldData[i] = (newData[i] * chg) + (oldData[i] * (1.0f - chg));
	}
}

int instability_fix = 2;

//---------------------------------------------------------------------------------------------------
// Fast inverse square-root
// See: http://en.wikipedia.org/wiki/Fast_inverse_square_root

float invSqrt(float x) 
{
	if (instability_fix == 0)
	{
		/* original code */
		float halfx = 0.5f * x;
		float y = x;
		long i = *(long*)&y;
		i = 0x5f3759df - (i>>1);
		y = *(float*)&i;
		y = y * (1.5f - (halfx * y * y));
		return y;
	}
	else if (instability_fix == 1)
	{
		/* close-to-optimal  method with low cost from http://pizer.wordpress.com/2008/10/12/fast-inverse-square-root */
		unsigned int i = 0x5F1F1412 - (*(unsigned int*)&x >> 1);
		float tmp = *(float*)&i;
		return tmp * (1.69000231f - 0.714158168f * x * tmp * tmp);
	}
	else
	{
		/* optimal but expensive method: */
		return 1.0f / sqrtf(x);
	}
}
